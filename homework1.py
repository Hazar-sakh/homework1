# Практическое задание по уроку "Строки и индексация строк"
example = 'Синтаксис'

print(example[0]) # выводим первый символ значения
print(example[-1]) # выводим последний символ значения
print(example[4:]) # выводим сиволы значения начиная с 4-го (отсчет с нуля)
print(example[::-1]) # выводим все значение в обратном порядке
print(example[1::2]) # выводим символы начиная с 1-го с шагом в через 1 (т.е. каждый второй)
